    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
    try {
        String nombre = txtNombre.getText();
        int nit = Integer.parseInt(txtNit.getText());
        String direccion = txtDireccion.getText();
        int telefono = Integer.parseInt(txtTelefono.getText());
        String email = txtEmail.getText();
        
        Empresa empresa = new Empresa(nombre, direccion, telefono, nit, email);
        
        controlador.agregarEmpresa(empresa);
        JOptionPane.showMessageDialog(null, "Empresa registrada");
        limpiarCampos();
        llenarTabla();
    } catch (EmpresaEx ex) {
        JOptionPane.showMessageDialog(null, ex.getMessage());
    }        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int nit = Integer.parseInt(txtNit.getText());
        
        Empresa empresa = controlador.buscarEmpresa(nit);
        
        if (empresa != null) {
            txtNombre.setText(empresa.getNombre());
            txtDireccion.setText(empresa.getDireccion());
            txtTelefono.setText(String.valueOf(empresa.getTelefono()));
            txtEmail.setText(empresa.getEmail());
        } else {
            JOptionPane.showMessageDialog(null, "Empresa NO encontrada");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String nombre = txtNombre.getText();
        int nit = Integer.parseInt(txtNit.getText());
        String direccion = txtDireccion.getText();
        int telefono = Integer.parseInt(txtTelefono.getText());
        String email = txtEmail.getText();
        
        Empresa empresa = new Empresa(nombre, direccion, telefono, nit, email);
        
        boolean ans = controlador.editarEmpresa(empresa);
        
        if (ans) {
            JOptionPane.showMessageDialog(null, "Información editada");
            llenarTabla();
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo editar la información");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int nit = Integer.parseInt(txtNit.getText());
        
        boolean ans = controlador.eliminarEmpresa(nit);
        
        if (ans) {
            JOptionPane.showMessageDialog(null, "Empresa eliminada");
            limpiarCampos();
            llenarTabla();
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo eliminar la empresa");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

