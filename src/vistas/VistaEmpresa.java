    private void llenarTabla() {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"NIT", "Nombre", "Telefono", "Email", "Direccion"});
        for (int i = 0; i < controlador.getEmpresas().size(); i++) {
            model.addRow(new Object[]{
                controlador.getEmpresas().get(i).getNit(),
                controlador.getEmpresas().get(i).getNombre(),
                controlador.getEmpresas().get(i).getTelefono(),
                controlador.getEmpresas().get(i).getEmail(),
                controlador.getEmpresas().get(i).getDireccion()
            });
        }
        tablaEmpresas.setModel(model);
    }
    
    private void limpiarCampos() {
       txtNombre.setText("");
       txtNit.setText("");
       txtDireccion.setText("");
       txtTelefono.setText("");
       txtEmail.setText("");
    }
    

