    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
    try {
        int id = Integer.parseInt(txtId.getText());
        String nombre = txtNombre.getText();
        String descripcion = txtDescripcion.getText();
        
        Categoria categoria = new Categoria(nombre, id, descripcion);
        
        controlador.agregarCategoria(categoria);
        JOptionPane.showMessageDialog(null, "Categoria a침adida");
        limpiarCampos();
        llenarTabla();
    } catch (CategoriaEx ex) {
        JOptionPane.showMessageDialog(null, ex.getMessage());
    }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int id = Integer.parseInt(txtId.getText());
        
        Categoria categoria = controlador.buscarCategoria(id);
        
        if (categoria != null) {
            txtNombre.setText(categoria.getNombre());
            txtDescripcion.setText(categoria.getDescripcion());
        } else {
            JOptionPane.showMessageDialog(null, "No existe esta categoria");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int id = Integer.parseInt(txtId.getText());
        String nombre = txtNombre.getText();
        String descripcion = txtDescripcion.getText();
        
        Categoria categoria = new Categoria(nombre, id, descripcion);
        
        boolean ans = controlador.editarCategoria(categoria);
        
        if (ans) {
            JOptionPane.showMessageDialog(null, "Informaci칩n editada");
            limpiarCampos();
            llenarTabla();
        } else {
            JOptionPane.showMessageDialog(null, "NO se pudo editar la informaci칩n");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int id = Integer.parseInt(txtId.getText());
        
        boolean ans = controlador.eliminarCategoria(id);
        
        if (ans) {
            JOptionPane.showMessageDialog(null, "Categoria eliminada");
            limpiarCampos();
            llenarTabla();
        } else {
            JOptionPane.showMessageDialog(null, "No se elimin칩");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void limpiarCampos() {
        txtId.setText("");
        txtNombre.setText("");
        txtDescripcion.setText("");
    }
    
    private void llenarTabla(){
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"ID", "Nombre", "Descripcion"});
        for (int i = 0; i < controlador.getCategorias().size(); i++) {
            model.addRow(new Object[]{
                controlador.getCategorias().get(i).getId(),
                controlador.getCategorias().get(i).getNombre(),
                controlador.getCategorias().get(i).getDescripcion()
            });
        }
        tablaCategoria.setModel(model);
    }

