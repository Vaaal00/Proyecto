    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try{
            String nombre = txtNombre.getText();
            String idTxt = txtId.getText();
            String contrasena = txtContrasena.getText();
            String cumple = txtCumple.getText();
            String email = txtEmail.getText();
            String rol = "Cliente";
            
            String telefonoUsu = txtTelefono.getText();
            

            if(idTxt.isEmpty() || idTxt.startsWith(" ") || nombre.isEmpty() || nombre.startsWith(" ") || cumple.isEmpty() || cumple.startsWith(" ") ||
                contrasena.isEmpty() || contrasena.startsWith(" ") ){
                JOptionPane.showMessageDialog(null, "Llena todos los campos");
                return;
            }

            if (!idTxt.matches("\\d+")) {
                JOptionPane.showMessageDialog(null, "El id debe de contener numeros");
                return;
            }

            if(!cumpleValidacion(cumple)){
                JOptionPane.showMessageDialog(null, "La forma correcta es aaaa-mm-dd");
                return;
            }

            if(nombre.matches(".*\\d+.*")) {
                JOptionPane.showMessageDialog(null, "Los numeros no se permiten en el nombre");
                return;
            }

            int id = Integer.parseInt(idTxt);
            LocalDate cumplean = LocalDate.parse(cumple);
            Usuario usuario = controlador.buscarUsuario(id);
            int telefono = Integer.parseInt(telefonoUsu);
            if (usuario.getId() == id) {
                 Usuario cliente = new Cliente(nombre, email, contrasena, rol, cumplean, id, telefono);
            controlador.actualizarUsuario(cliente);
                
            }
           
            
            JOptionPane.showMessageDialog(null, "Usuario actualizado");
            LlenarTabla();
            limpiarCampos();
        }catch(FechaValidacionEx |  DateTimeParseException ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (UsuarioNoEncontradoEx ex) {
            Logger.getLogger(RegistroCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

